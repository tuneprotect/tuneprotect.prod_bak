                    <li>4. To supervise and oversee the business operations for ensuring that the Company is fully
                        complied with laws and regulations, also to monitor the capital adequacy including any incidents
                        or significant matters which affect business operations. Moreover, to encourage the Company to
                        treat the insureds, customers and stakeholders equally.
                    </li>
                </ul>

                <h3>Roles and Responsibilities of Executives and Managements</h3>

                <ul>
                    <li>1. Implement the strategies and policies efficiency by taking into account of the Company’s
                        long-term values and sustainable business operations.
                    </li>
