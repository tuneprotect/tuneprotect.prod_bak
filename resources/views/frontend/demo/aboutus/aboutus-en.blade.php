                <h2>Internal Controls</h2>

                <p>Internal control principle is as part of the corporate good governance and in furtherance of the safe
                    and sound operation, which is set out and deemed as basic principle in order to achieve sustainable
                    and robust business amidst rapid changes. The internal control covers information technology, all
                    business operations, financial management process, financial report, risk management and compliance.
                    The internal control structure and its components include but not limited to the followings:
                </p>

                <ul>
                    <li>1. The Board is ultimately responsible for ensuring that the Company has in place with effective
                        internal control system, and oversee internal control system in the Company appropriately.
                    </li>
                    <li>2. Internal control management performed by business units and the Management as the 1st Line of
                        Responsibilities for ensuring that the internal control has undertaken, operated and maintained
                        efficiently.

                    </li>
                    <li>3. Risk Management and Compliance functions facilitate and monitor the implementation of
                        effective risk management and monitoring of various compliance matters with applicable laws and
                        regulations as the 2nd Line of Responsibilities.
                    </li>
                    <li>4. Internal Audit function governs and oversee the effectiveness of the first line and the
                        second line of defence.
                    </li>
                    <li>5. To support the risk management culture and compliance system with the aim of having the
                        appropriate internal control principle.
                    </li>
                    <li>6. To protect the customer information and establish the measurement of information protection
                        for ensuring that their data have been collected, used and stored appropriately under the data
                        privacy policy.
                    </li>
                </ul>

